var mDisabledWebPageSuffix = make(map[string]struct{}, len(disabledWebPageSuffix))
var mDomainSuffix = make(map[string]struct{}, len(domainSuffix))

func init() {
	for _, v := range domainSuffix {
		mDomainSuffix[v] = struct{}{}
	}
	for _, v := range disabledWebPageSuffix {
		mDisabledWebPageSuffix[v] = struct{}{}
	}
}

//isDomainSuffix
